public class TreinoWhile {
    public void treinoWhile (){
    //quantidade de dias para o primeiro ciclo do treino de adaptação
        System.out.println("\nQuantidade de dias do treino de adaptação:");
    int adaptacao = 1;
        while (adaptacao <= 20) {
        System.out.println ("\nDia: " + adaptacao + " concluído com sucesso." );
        adaptacao++;
    }
        System.out.println("\nParabéns! Você concluiu seu treino de adaptação.");
 }
}
